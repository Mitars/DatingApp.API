namespace DatingApp.API.Helpers
{
    /// <summary>
    /// The Cloudinary settings class.
    /// </summary>
    public class CloudinarySettings
    {
        /// <summary>
        /// Gets or sets the Cloudinary cloud name.
        /// </summary>
        public string CloudName { get; set; }

